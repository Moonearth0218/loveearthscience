import streamlit as st

st.set_page_config(
    page_title="🌏 지구과학 활동 허브",
    page_icon="🌏",
    layout="wide"
)

st.title("🌏 지구과학 활동 허브")
st.subheader("고등학교 대상 지구과학 수업을 위한 인터랙티브 프로그램")

st.markdown("""
이 웹페이지는 **지구과학 수업에서 바로 활용 가능한 활동들**을 한 곳에 모은 허브입니다.  
학생들이 데이터를 직접 다루고, 시각화하며, 과학적 사고로 해석할 수 있도록 돕는 **실습 중심** 구성을 지향합니다.

**지금 제공되는 활동**
- **국외지진목록 시각화**: KMA(기상청) 국외지진 데이터를 업로드하여, **전세계 지도 위에 규모(M)별로 시각화**합니다.  
  규모는 *정수 구간(예: 2.0–2.9)*으로 나뉘어 **같은 구간은 같은 색**, **작을수록 파란색 → 클수록 붉은색**으로 표현됩니다.  
  기간/규모/깊이/지역 필터를 통해 탐구 질문을 스스로 세우고 답을 찾는 학습이 가능해요.

> 앞으로 **화산 활동 지도**, **기상·기후 데이터 분석**, **위성영상 판독** 등 다양한 지구과학 활동이 추가될 예정입니다.
""")

st.divider()

st.markdown("### 🚀 바로 시작하기")
col1, col2 = st.columns([1,1])

with col1:
    st.markdown("**전세계 지진 시각화**")
    st.caption("KMA 국외지진목록 파일(.xls/HTML 테이블)을 업로드하고, 규모/깊이/기간/지역으로 필터링하여 분석하세요.")
    # Streamlit 1.29+ 에서 제공되는 페이지 이동 API
    go = st.button("🗺️ 지진 월드맵으로 이동")
    if go:
        try:
            st.switch_page("pages/01_지진_월드맵.py")
        except Exception:
            st.info("왼쪽 사이드바에서 **‘01_지진_월드맵’** 페이지를 선택해 주세요.")

with col2:
    st.markdown("**사용 팁**")
    st.write("""
    - 데이터 파일은 학교 네트워크 환경에 따라 다운로드/업로드 속도 차이가 있으니 수업 전에 한 번 테스트하세요.
    - 지도에서 드래그/확대 후 **툴팁**을 통해 시간·규모·깊이·지역 정보를 세부 확인할 수 있습니다.
    - 활동지와 연계하여 **탐구 질문(가설) → 데이터 필터링 → 증거 제시** 흐름으로 수업을 설계해 보세요.
    """)

st.divider()
st.markdown("""
**데이터 출처 안내**  
- 국외지진목록은 기상청(KMA)에서 제공하는 자료를 기반으로 하며, 수업 목적으로만 사용하세요.  
- 업로드하는 파일은 브라우저 세션 범위에서만 처리됩니다.
""")
